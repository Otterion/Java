package prac5.task2;

import java.awt.*;

public abstract class Shape {
    protected int x,y,w,h;
    protected Color color;

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getW() {
        return w;
    }

    public int getH() {
        return h;
    }

    public Color getColor() {
        return color;
    }
}
